import { body, param } from 'express-validator';
import mongoose from 'mongoose';

export const createBookingValidation = [
  body('eventId')
    .exists()
    .withMessage('eventId is required')
    .bail()
    .custom((v) => mongoose.Types.ObjectId.isValid(v))
    .withMessage('Invalid eventId'),
  body('quantity').exists().isInt({ min: 1 }).withMessage('quantity is required and must be >= 1'),
  // Reject client-supplied managed fields
  body('bookingNumber').optional().custom(() => {
    throw new Error('bookingNumber is generated by the system');
  }),
  body('userId').optional().custom(() => {
    throw new Error('userId is taken from the authenticated user');
  }),
  body('totalAmount').optional().custom(() => {
    throw new Error('totalAmount is calculated by the system');
  }),
  body('bookingStatus').optional().custom(() => {
    throw new Error('bookingStatus is managed by the system');
  }),
  body('paymentStatus').optional().custom(() => {
    throw new Error('paymentStatus is managed by the system');
  }),
];

export const cancelBookingValidation = [
  param('id').custom((v) => mongoose.Types.ObjectId.isValid(v)).withMessage('Invalid booking id'),
];