import { body, param, query } from 'express-validator';
import mongoose from 'mongoose';

export const createEventValidation = [
  body('stadiumId')
    .exists()
    .withMessage('stadiumId is required')
    .bail()
    .custom((val) => mongoose.Types.ObjectId.isValid(val))
    .withMessage('Invalid stadiumId'),
  body('title').isString().trim().notEmpty().withMessage('Title is required'),
  body('description').optional().isString().trim(),
  body('sport').optional().isString().trim(),
  body('organizer').optional().isString().trim(),
  body('bannerImage').optional().isObject(),
  body('bannerImage.url').optional().isURL().withMessage('bannerImage.url must be a valid URL'),
  body('startDateTime').exists().isISO8601().withMessage('startDateTime is required and must be a valid date'),
  body('endDateTime').exists().isISO8601().withMessage('endDateTime is required and must be a valid date'),
  body('ticketBookingStart').optional().isISO8601(),
  body('ticketBookingEnd').optional().isISO8601(),
  body('totalSeats').exists().isInt({ min: 0 }).withMessage('totalSeats required and must be integer >= 0'),
  body('availableSeats').optional().custom(() => {
    throw new Error('availableSeats is managed by the system');
  }),
  body('price').optional().isFloat({ min: 0 }).withMessage('price must be >= 0'),
  body('status').optional().custom(() => {
    throw new Error('status is managed by the system on create');
  }),
  // reject client-supplied slug
  body('slug').optional().custom(() => {
    throw new Error('slug is generated by the system');
  }),
];

export const updateEventValidation = [
  param('id').custom((val) => mongoose.Types.ObjectId.isValid(val)).withMessage('Invalid event id'),
  body('title').optional().isString().trim(),
  body('description').optional().isString().trim(),
  body('sport').optional().isString().trim(),
  body('organizer').optional().isString().trim(),
  body('bannerImage').optional().isObject(),
  body('bannerImage.url').optional().isURL(),
  body('startDateTime').optional().isISO8601(),
  body('endDateTime').optional().isISO8601(),
  body('ticketBookingStart').optional().isISO8601(),
  body('ticketBookingEnd').optional().isISO8601(),
  body('totalSeats').optional().isInt({ min: 0 }).withMessage('totalSeats must be integer >= 0'),
  body('availableSeats').optional().custom(() => {
    throw new Error('availableSeats is managed by the system');
  }),
  body('price').optional().isFloat({ min: 0 }),
  body('status').optional().isIn(['scheduled', 'live', 'completed', 'cancelled']),
  body('slug').optional().custom(() => {
    throw new Error('Slug is immutable and cannot be changed');
  }),
];

export const getAllValidation = [
  query('stadiumId').optional().custom((val) => mongoose.Types.ObjectId.isValid(val)).withMessage('Invalid stadiumId'),
  query('sport').optional().isString(),
  query('city').optional().isString(),
  query('status').optional().isString(),
  query('upcoming').optional().isBoolean(),
  query('completed').optional().isBoolean(),
  query('startDateFrom').optional().isISO8601(),
  query('startDateTo').optional().isISO8601(),
  query('minPrice').optional().isFloat({ min: 0 }),
  query('maxPrice').optional().isFloat({ min: 0 }),
  query('q').optional().isString(),
  query('page').optional().isInt({ min: 1 }),
  query('limit').optional().isInt({ min: 1, max: 100 }),
];