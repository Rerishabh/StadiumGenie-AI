import { body, param } from 'express-validator';
import mongoose from 'mongoose';

export const createPaymentValidation = [
  body('bookingId')
    .exists()
    .withMessage('bookingId is required')
    .bail()
    .custom((v) => mongoose.Types.ObjectId.isValid(v))
    .withMessage('Invalid bookingId'),
  body('paymentMethod')
    .exists()
    .withMessage('paymentMethod is required')
    .bail()
    .isIn(['card', 'upi', 'netbanking', 'wallet', 'cash'])
    .withMessage('Invalid paymentMethod'),
  // Reject client-supplied managed fields
  body('paymentReference').optional().custom(() => {
    throw new Error('paymentReference is generated by the system');
  }),
  body('paymentStatus').optional().custom(() => {
    throw new Error('paymentStatus is managed by the system');
  }),
  body('amount').optional().custom(() => {
    throw new Error('amount is calculated by the system');
  }),
  body('userId').optional().custom(() => {
    throw new Error('userId is taken from the authenticated user');
  }),
];

export const getPaymentValidation = [
  param('id').custom((v) => mongoose.Types.ObjectId.isValid(v)).withMessage('Invalid payment id'),
];