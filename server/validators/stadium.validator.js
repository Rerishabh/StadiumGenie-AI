import { body, param, query } from 'express-validator';
import mongoose from 'mongoose';

export const createStadiumValidation = [
  body('name').isString().trim().notEmpty().withMessage('Name is required'),
  body('city').isString().trim().notEmpty().withMessage('City is required'),
  body('capacity')
    .isInt({ min: 1 })
    .withMessage('Capacity is required and must be a positive integer'),
  body('state').optional({ values: 'null' }).isString().trim(),
  body('country').optional({ values: 'null' }).isString().trim(),
  body('address').optional({ values: 'null' }).isString().trim(),
  body('description').optional({ values: 'null' }).isString().trim(),
  body('imageUrl')
    .optional({ values: 'null' })
    .isURL()
    .withMessage('imageUrl must be a valid URL'),
  body('facilities').optional().isArray().withMessage('facilities must be an array'),
  body('facilities.*').optional().isString().trim(),
  body('sportsSupported').optional().isArray(),
  body('sportsSupported.*').optional().isString().trim(),
  body('amenities').optional().isArray(),
  body('amenities.*').optional().isString().trim(),
  body('images').optional().isArray(),
  body('images.*.url').optional().isURL().withMessage('Image url must be a valid URL'),
  body('images.*.alt').optional().isString(),
  body('latitude')
    .optional({ values: 'null' })
    .isFloat({ min: -90, max: 90 })
    .withMessage('latitude must be between -90 and 90'),
  body('longitude')
    .optional({ values: 'null' })
    .isFloat({ min: -180, max: 180 })
    .withMessage('longitude must be between -180 and 180'),
  body('location')
    .optional()
    .custom((val) => {
      if (typeof val !== 'object' || val === null) return false;
      const { latitude, longitude } = val;
      if (typeof latitude !== 'number' || typeof longitude !== 'number') return false;
      if (latitude < -90 || latitude > 90) return false;
      if (longitude < -180 || longitude > 180) return false;
      return true;
    })
    .withMessage('Location must be { latitude: number, longitude: number }'),
  // rating and slug must not be set by client
  body('rating').optional().custom(() => {
    throw new Error('Rating is read-only');
  }),
  body('slug').optional().custom(() => {
    throw new Error('Slug is generated by the system');
  }),
];

